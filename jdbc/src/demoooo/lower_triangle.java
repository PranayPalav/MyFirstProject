package demoooo;

public class lower_triangle {
	public static void main(String args[]){
		for(int i=5;i>0;i--){
			for(int k=i-1;k<=4;k++){
				System.out.print(" ");
			}
		 for(int j=(2*(i-1)-1);j>0;j--){
			 System.out.print("*");
		 }
		
		 System.out.println();
			
		}
		
	}

}
